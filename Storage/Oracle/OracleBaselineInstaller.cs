    public const int BaselineVersion = 49;

    public static void Install(OracleConnection conn)
    {
        foreach (var batch in GetTableStatements())
            ExecuteIgnoringExists(conn, batch);

        foreach (var ix in GetIndexStatements())
            ExecuteIgnoringExists(conn, ix);

        TryCreateContextIndexes(conn);
        SeedDefaults(conn);
    }

    private static void ExecuteIgnoringExists(OracleConnection conn, string sql)
    {
        try
        {
            using var cmd = OracleSql.CreateCommand(conn, sql);
            cmd.ExecuteNonQuery();
        }
        catch (OracleException ex) when (ex.Number is 955 or 1408 or 1430 or 2264 or 2261 or 2275 or 942)
        {
            Log.Debug(ex, "Skipped DDL (already applied): {Sql}", sql.Length > 120 ? sql[..120] + "..." : sql);
        }
    }

