    private static void TryCreateContextIndexes(OracleConnection conn)
    {
        string[] stmts =
        [
            """CREATE INDEX idx_ctx_documents_ocr ON documents (ocr_text) INDEXTYPE IS CTXSYS.CONTEXT""",
            """CREATE INDEX idx_ctx_documents_snippet ON documents (snippet) INDEXTYPE IS CTXSYS.CONTEXT""",
            """CREATE INDEX idx_ctx_documents_notes ON documents (notes) INDEXTYPE IS CTXSYS.CONTEXT"""
        ];
        foreach (var sql in stmts)
        {
            try
            {
                using var cmd = OracleSql.CreateCommand(conn, sql);
                cmd.ExecuteNonQuery();
            }
            catch (OracleException ex)
            {
                Log.Warning(ex, "Oracle Text index not created (missing privileges or lexer); full-text search may use LIKE fallback");
                break;
            }
        }

        try
        {
            using var cmd = OracleSql.CreateCommand(conn,
                """CREATE INDEX idx_ctx_documents_doctype ON documents (document_type) INDEXTYPE IS CTXSYS.CONTEXT""");
            cmd.ExecuteNonQuery();
        }
        catch (OracleException ex)
        {
            Log.Warning(ex, "Oracle Text index on document_type skipped");
        }
    }

